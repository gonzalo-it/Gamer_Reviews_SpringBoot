package com.gamer.api.model;

public class ProxJuego {
    private int id;
    private String nombre;
    private String fotoUrl;
    private String fotoVieja;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getFotoUrl() {
		return fotoUrl;
	}
	public void setFotoUrl(String fotoUrl) {
		this.fotoUrl = fotoUrl;
	}
	public String getFotoVieja() {
		return fotoVieja;
	}
	public void setFotoVieja(String fotoVieja) {
		this.fotoVieja = fotoVieja;
	}

 
}